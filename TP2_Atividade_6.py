# Faça um algoritmo que leia um caractere e informe se o mesmo é uma vogal ou não.

var = str(input("Digite uma letra: "))
if(var == "a"):
    print(var," é uma vogal" )
elif (var == "e"):
    print(var," é uma vogal" )
elif (var == "i"):
    print(var," é uma vogal" )
elif (var == "o"):
    print(var," é uma vogal" )
elif (var == "u"):
    print(var," é uma vogal" )
else:
    print(var," não é uma vogal")
print("Fim do programa")